package model;

public class LastButton {
	private int value;

	public LastButton() {

		this.value = 1;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
	public void setValueTo_1() {
		this.setValue(1);
	}
	public void setValueTo_2() {
		this.setValue(2);
	}
	public void setValueTo_3() {
		this.setValue(3);
	}
	public void setValueTo_4() {
		this.setValue(4);
	}
}
