
public class Primitive_Data_Type {
	public static void main(String[] args) {
		String tenSach = "Lap Trinh Java";
		int namXuatBan = 2022;
		double giaTien = 15.5;
		boolean conSach = true;
		char maKho = 'M';
		System.out.println("Ten sach: "+ tenSach);
		System.out.println("Nam xuat ban: "+ namXuatBan);
		System.out.println("Thanh tien: " + giaTien);
		System.out.println("Tinh trang hang: " + conSach);
		System.out.println("Ma kho sach: " + maKho);
	}
}
